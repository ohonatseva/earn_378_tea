// Helper function to calculate the square of a number
function square(num) {
    return num * num;
  }
  
  // Helper function to check if a number is even
  function isEven(num) {
    return num % 2 === 0;
  }
  
  // Helper function to generate a random number within a range
  function getRandomNumber(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
  }
  
  // Function to get the 88n77 value
  function get88n77() {
    return "tea567";
  }
  
  // Function to get the ganthu value
  function ganthu() {
    return "ganthu";
  }
  
  // Function to add a word to a string
  function addWord(str, word) {
    return str + " " + word;
  }
  
  // Example usage:
  console.log(square(5)); // Output: 25
  console.log(isEven(6)); // Output: true
  console.log(getRandomNumber(1, 10)); // Output: Random number between 1 and 10
  console.log(get88n77()); // Output: 88n77
  console.log(ganthu()); // Output: ganthu
  console.log(addWord("Hello", "World")); // Output: Hello World
  
